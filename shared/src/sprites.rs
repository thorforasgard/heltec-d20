/// ASCII art sprites for each die type.
/// Each die has:
///   - A "face" template with `{}` where the number goes
///   - Multiple animation frames for the tumbling effect
///   - Designed to be recognizable as the actual polyhedron shape

/// Get the static face art for a die result
pub fn die_face(die_name: &str, value: u16) -> &'static [&'static str] {
    match die_name {
        "d4" => d4_face(value),
        "d6" => d6_face(value),
        "d8" => d8_face(value),
        "d10" => d10_face(value),
        "d12" => d12_face(value),
        "d20" => d20_face(value),
        "d100" => d100_face(value),
        _ => d20_face(value),
    }
}

/// Get tumbling animation frames for a die
pub fn tumble_frames(die_name: &str) -> &'static [&'static [&'static str]] {
    match die_name {
        "d4" => &D4_TUMBLE,
        "d6" => &D6_TUMBLE,
        "d8" => &D8_TUMBLE,
        "d10" => &D10_TUMBLE,
        "d12" => &D12_TUMBLE,
        "d20" => &D20_TUMBLE,
        "d100" => &D100_TUMBLE,
        _ => &D20_TUMBLE,
    }
}

